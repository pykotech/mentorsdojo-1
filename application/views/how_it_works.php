<h2>How it Works</h2>
<p>1. Browse our <a href="http://mentorsdojo.com/">list of mentors</a> then check out their profiles, what they do and how they can help you out.</p>

<p>2. Click on the "Message" button. In the message popup, introduce yourself to the mentor, tell the mentor why you'd like to meet them and share your goals for your startup with him. Don't forget to suggest a time and date when you can meet up.</p>

<p>3. If the mentor is good to go, he or she will respond in kind and you can continue to conversation either online or offline. Once you meet, this is the time to talk about the help you need and the type of guidance you are looking for. This also the best time to talk more about your goals for your startup.</p>

<p>4. Send feedback to the mentor every so often to keep him updated on your progress. You can also set up further meetings as well. We suggest meeting for coffee once a month.</p>

<p>5. Success! You've reached your goals. It's also possible that you've changed your goals but don't worry, you can always go back to step 1 and get more help.</p>
