<p>Thanks for accepting our invite to become a mentor for Mentors Dojo! Before we begin, here are the values that every mentor should uphold in the Dojo. Through this set of shared values, our goal is to strengthen every mentor-mentee relationship so that it benefits everyone involved. </p>
<br />
<p>1. <span class="color-text">Listen </span>- Keep an open mind. Listen to the mentee, understand his situation and empathize with him.</p><br />

<p>2. <span class="color-text">Establish Trust </span>- Everything said should be held in the strictest confidence, unless explicitly stated. Don't share this with others and don't use it to gain unfair advantage.</p><br />

<p>3. <span class="color-text">Be Honest </span>- Share your experiences and let them know what worked and didn't work for you. Provide constructive criticism. Be brutally honest if the mentee needs to hear the truth.</p><br />

<p>4. <span class="color-text">Challenge </span>- Encourage the mentee to take the next step, even if it might seem daunting. Especially if it seems daunting.</p><br />

<p>5. <span class="color-text">Support </span>- Help the mentee out the best way you can. Don't become a crutch though: you're the handlebars not the training wheels.</p><br />

<p>6. <span class="color-text">Respect </span>- Be professional. Arrive and start on time, commit to meetings and stay away from off-limits topics. Also, call out unprofessional behavior if necessary.</p><br />

<p>7. <span class="color-text">Lead </span>- Walk the talk. Inspire others to succeed through your actions.</p><br />

<p>8. <span class="color-text">Be Free (Like Beer) </span>- Don't ask for compensation of any sort. Getting offered a piece of the pie by the mentee himself is a different story though. Expect to be paid back with data (aka learning)</p><br />

